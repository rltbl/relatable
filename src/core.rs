#[derive(Clone, Debug, Serialize, Deserialize)]
pub struct Table {
    pub name: String,
    // A view to use when displaying this table, or empty if none.
    pub view: String,
    // The history_id of the most recent update to this table.
    pub change_id: usize,
    pub columns: HashMap<String, Column>,
    pub editable: bool,
}

impl Table {
    pub async fn ensure_view_created(&mut self, rltbl: &Relatable) -> Result<Vec<Column>> {
        tracing::debug!("Entering ensure_view_created({rltbl:?})");
        let (columns, meta_columns) = rltbl.fetch_all_columns(&self.name).await?;
        self.view = format!("{}_default_view", self.name);
        let id_col = match meta_columns.iter().any(|c| c.name == "_id") {
            false => r#"rowid"#, // This *must* be lowercase.
            true => r#"_id"#,
        };
        let order_col = match meta_columns.iter().any(|c| c.name == "_order") {
            false => r#"rowid"#, // This *must* be lowercase.
            true => r#"_order"#,
        };

        // Note that '?' parameters are not allowed in views so we must hard code them:
        let sql = format!(
            r#"CREATE VIEW IF NOT EXISTS "{view}" AS
                 SELECT
                   {id_col} AS _id,
                   {order_col} AS _order,
                   (SELECT '[' || GROUP_CONCAT("after") || ']'
                      FROM (
                        SELECT "after"
                        FROM "history"
                        WHERE "table" = '{table}'
                        AND "after" IS NOT NULL
                        AND "row" = {id_col}
                        ORDER BY "history_id"
                     )
                   ) AS "_history",
                   (SELECT NULLIF(
                      JSON_GROUP_ARRAY(
                        JSON_OBJECT(
                          'column', "column",
                          'value', "value",
                          'level', "level",
                          'rule', "rule",
                          'message', "message"
                        )
                      ),
                      '[]'
                    ) AS "_message"
                      FROM "message"
                      WHERE "table" = '{table}'
                      AND "row" = {id_col}
                      ORDER BY "column", "message_id"
                   ) AS "_message",
                   {columns}
                 FROM "{table}""#,
            table = self.name,
            view = self.view,
            columns = columns
                .iter()
                .map(|c| format!(r#""{}""#, c.name))
                .collect::<Vec<_>>()
                .join(", "),
        );
        rltbl.connection.query(&sql, None).await?;
        Ok(columns)
    }

    pub fn get_column_attribute(&self, column: &str, attribute: &str) -> Option<String> {
        self.columns.get(column).and_then(|col| match attribute {
            "table" => Some(col.table.to_string()),
            "column" => Some(col.name.to_string()),
            "label" => match &col.label {
                None => None,
                Some(label) if label == "" => None,
                Some(_) => col.label.clone(),
            },
            "description" => match &col.description {
                None => None,
                Some(description) if description == "" => None,
                Some(_) => col.description.clone(),
            },
            "nulltype" => match &col.nulltype {
                None => None,
                Some(nulltype) if nulltype == "" => None,
                Some(_) => col.nulltype.clone(),
            },
            _ => None,
        })
    }
}

